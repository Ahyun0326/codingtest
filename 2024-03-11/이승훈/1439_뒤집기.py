"""
1439. 뒤집기

0001100 -> 변화횟수: 2, 뒤집는횟수:1
00011001 -> 변화횟수: 3, 뒤집는 횟수: 2
11101101 -> 변화횟수: 4, 뒤집는 횟수: 2
111011010 -> 변화 횟수: 5, 뒤집는 횟수: 3
11001100110011000001 -> 변화횟수: 8, 뒤집는 횟수: 4
"""

nums = list(input())
k = 0

for i in range(len(nums) - 1):
    if nums[i] != nums[i + 1]:
        k += 1

print((k + 1) // 2)
